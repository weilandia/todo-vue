<template>
  <div id="app">
    <TodoList v-bind:todos="todos"></TodoList>
    <CreateTodo v-on:createTodo="createTodo"></CreateTodo>
  </div>
</template>

<script>
import TodoList from './components/TodoList';
import CreateTodo from './components/CreateTodo';

export default {
  components: {
    TodoList,
    CreateTodo,
  },
  data() {
    return {
      todos: [{
        title: 'Todo A',
        project: 'Project A',
        done: false,
      }, {
        title: 'Todo B',
        project: 'Project B',
        done: true,
      }, {
        title: 'Todo C',
        project: 'Project C',
        done: false,
      }, {
        title: 'Todo D',
        project: 'Project D',
        done: false,
      }],
    };
  },
  methods: {
    createTodo(todo) {
      this.todos.push({
        ...todo,
        done: false,
      });
    },
  },
};
</script>

